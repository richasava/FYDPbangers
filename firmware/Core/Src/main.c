/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */
  printf("\r\n--- MPU-6050 bring-up ---\r\n");

  uint8_t who = 0;
  HAL_I2C_Mem_Read(&hi2c1, MPU_ADDR, REG_WHO_AM_I, 1, &who, 1, 100);
  printf("WHO_AM_I = 0x%02X (expect 0x68)\r\n", who);

  if (who == 0x68) {
      uint8_t wake = 0x00; // clear sleep bit
      HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, REG_PWR_MGMT1, 1, &wake, 1, 100);
      printf("MPU awake, streaming...\r\n");
  } else {
      printf("MPU not found - check wiring/pullups\r\n");
  }
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  // this reads sensor and converts to tilt angles. single i2c read can pull all at once
	  uint8_t buf[6];

	  HAL_StatusTypeDef st = HAL_I2C_Mem_Read(&hi2c1, MPU_ADDR, REG_ACCEL_XOUT, 1, buf, 6, 100);
	  if (st != HAL_OK) {
	      printf("I2C recover\r\n");
	      i2c_bus_recover();
	      HAL_Delay(10);
	      continue;
	  }

	  // added bus recovery when we detect all 0's (probably from VCC drop)
	  if (buf[0]==0 && buf[1]==0 && buf[2]==0 && buf[3]==0 && buf[4]==0 && buf[5]==0) {
	      printf("sensor asleep, re-waking\r\n");
	      uint8_t wake = 0x00;
	      HAL_I2C_Mem_Write(&hi2c1, MPU_ADDR, REG_PWR_MGMT1, 1, &wake, 1, 100);
	      HAL_Delay(10);
	      continue;
	  }

	  // grab 6 bytes from MPU starting at register 0x3B.
	  // this is split into x,y,z and and split across 2 bytes.
	  int16_t ax = (buf[0] << 8) | buf[1];
	  int16_t ay = (buf[2] << 8) | buf[3];
	  int16_t az = (buf[4] << 8) | buf[5];

	  // tilt math. referenced data sheet and \ axis comes as two separate bytes
	  // this stitches them back into one signed 16-bit number
	  float axg = ax / 16384.0f, ayg = ay / 16384.0f, azg = az / 16384.0f;
	  float roll  = atan2f(ayg, azg) * 57.2958f;
	  float pitch = atan2f(-axg, sqrtf(ayg*ayg + azg*azg)) * 57.2958f;

	  // packing roll and pitch into a frame..............
	  char frame[40];
	  int len = snprintf(frame, sizeof(frame), "R:%.1f,P:%.1f\n", roll, pitch);
	  HAL_UART_Transmit(&huart1, (uint8_t*)frame, len, 100);

	  printf("roll=%6.1f  pitch=%6.1f\r\n", roll, pitch);
	  HAL_Delay(500);   // 20 Hz
  }
  /* USER CODE END 3 */
}

